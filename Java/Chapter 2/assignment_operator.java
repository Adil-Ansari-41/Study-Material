public class assignment_operator {
    public static void main(String[] args) {
        
        int a = 5;
        int b = 20;

        a += 10 ; // a = a+10
        b -= 5  ; // b = b-5

        System.out.println(a);
        System.out.println(b);

        a *= 2; //a = a*2
        b /= 2; //b = b*2

        System.out.println(a);
        System.out.println(b);

        a %= 4; //a = a%4

        System.out.println(a);
    }
    
}
