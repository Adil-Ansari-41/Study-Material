public class relational_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
       
        // equal to ==
        System.out.println(a == b);
        
        // not equal to ==
        System.out.println(a != b);
        
        // greater than  >
        System.out.println(a > b);

        // less than  <
        System.out.println(a < b);

        // greater than equal to  >=
        System.out.println(a >= b);

         // less than equal to  <=
         System.out.println(a <= b);
    }
    
}
