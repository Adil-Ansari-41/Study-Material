public class type_conversion {
    public static void main(String[] args) {
        
        int a = 20;
        long b = a;
        System.out.println(b);
        
        char ch = 'a';
        int number = ch;
        char ch2 = 'b';
        int number2 = ch2;
        System.out.println(number); //97
        System.out.println(number2); //98
    }
}
 